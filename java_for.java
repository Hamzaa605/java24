import java.util.*;
class java_for
{
	public static void main(String args[])
	{
		/*inatializatin;condition;inc/dec*/
	System.out.println("increment");	
		for(int i=1; i<=5;i++)
		{
		 System.out.println(i);	
		}
	System.out.println("decrement");	
		for(int n=5; n>=1;n--)
		{
		 System.out.println(n);	
		}
	}
	
}